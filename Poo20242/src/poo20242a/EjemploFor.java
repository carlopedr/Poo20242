

public class EjemploFor {
    public static void main(String[] args) {
        System.out.println("Con for...");
        for (int i=1;i<=5;i++){
            System.out.println("i: "+i);
        }
        //Comparado con while
        System.out.println("Con while...");
        int i=1;
        while(i<=5){
            System.out.println("i: "+i);
            i++;
        }
    }
}
